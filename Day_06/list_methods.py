# List Methods

nums = [5, 2, 9, 1, 7]

print("Original list:", nums)

nums.append(10)
print("After append:", nums)

nums.insert(2, 99)
print("After insert:", nums)

nums.remove(2)
print("After remove:", nums)

nums.pop()
print("After pop:", nums)

nums.sort()
print("After sort:", nums)

nums.reverse()
print("After reverse:", nums)
