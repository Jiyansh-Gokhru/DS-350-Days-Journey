# List Basics

numbers = [10, 20, 30, 40, 50]

print(numbers)
print("Length:", len(numbers))

# Indexing
print("First element:", numbers[0])
print("Last element:", numbers[-1])

# Update element
numbers[2] = 99
print("Updated list:", numbers)

# Loop through list
for num in numbers:
    print(num)
