# Dictionary of subjects and marks
marks = {
    "Maths": 85,
    "Physics": 78,
    "Chemistry": 82,
    "English": 90
}

# Print all keys
print("Subjects:")
print(marks.keys())

# Print all values
print("\nMarks:")
print(marks.values())

# Print key-value pairs
print("\nSubject and Marks:")
print(marks.items())
