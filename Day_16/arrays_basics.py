import numpy as np

# 1D array
arr1 = np.array([10, 20, 30, 40, 50])
print(arr1)
print(type(arr1))

# 2D array
arr2 = np.array([[1, 2, 3],
                 [4, 5, 6]])
print(arr2)

# Array properties
print("Shape:", arr2.shape)
print("Size:", arr2.size)
print("Data Type:", arr2.dtype)
